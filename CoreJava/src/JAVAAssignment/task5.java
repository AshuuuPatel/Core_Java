// Write a program in Java to display the pattern like right angle triangle with a number.

package JAVAAssignment;

public class task5 {
	public static void main(String[] args) {
	   	int n = 5;

	   	for (int i = 1; i <= n; i++) {
	       	for (int j = 1; j <= i; j++) {
	           	System.out.print(j);
	       	}
	       	System.out.println();
	   	}
	}
}
